package simplepojo;

/**
 * @author Ivo Woltring
 */
public class ZipCode {
    private int zipInt;
    private String zipAlpha;

    public int getZipInt() {
        return zipInt;
    }

    public void setZipInt(final int zipInt) {
        this.zipInt = zipInt;
    }

    public String getZipAlpha() {
        return zipAlpha;
    }

    public void setZipAlpha(final String zipAlpha) {
        this.zipAlpha = zipAlpha;
    }
}
